.MODEL SMALL
.STACK 100h
.DATA

MSG DB 'Enter a number: $'
POSITIVE_MSG DB 'Positive$'
NEGATIVE_MSG DB 'Negative$'

.CODE

MAIN PROC

MOV AX,@DATA
MOV DS,AX

LEA DX, MSG
MOV AH, 9
INT 21h

MOV AH, 1
INT 21h 
SUB AL, 30h ; convert to int

CMP AL, 0 ; compare AL to 0

JG PRINT_POSITIVE ; AL > 0 
JL PRINT_NEGATIVE ; AL < 0

Exit:
MOV AH,4CH
INT 21h

PRINT_POSITIVE:
LEA DX, POSITIVE_MSG
MOV AH, 9
INT 21h

JMP Exit

PRINT_NEGATIVE:
LEA DX, NEGATIVE_MSG
MOV AH, 9
INT 21h

JMP Exit

MAIN ENDP
END MAIN

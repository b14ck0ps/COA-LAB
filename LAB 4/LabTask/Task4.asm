.MODEL SMALL
.STACK 100h
.DATA

MSG DB 'Enter a number: $'
LESS_THAN_MSG DB 'Less than 5$'
GREATER_THAN_MSG DB 'Greater than 5$'
EQUAL_TO_MSG DB 'Equal to 5$'

.CODE

MAIN PROC

MOV AX,@DATA
MOV DS,AX

LEA DX, MSG
MOV AH, 9
INT 21h

MOV AH, 1
INT 21h 
SUB AL, 30h ; Convert to int

MOV CL, 5
CMP AL, CL ; Compare the user input with CL

JE PRINT_EQUAL_TO ; == 5
JL PRINT_LESS_THAN ; < 5
JG PRINT_GREATER_THAN ; > 5

Exit:
MOV AH,4CH
INT 21h

PRINT_EQUAL_TO:
LEA DX, EQUAL_TO_MSG
MOV AH, 9
INT 21h

JMP Exit

PRINT_LESS_THAN:
LEA DX, LESS_THAN_MSG
MOV AH, 9
INT 21h

JMP Exit

PRINT_GREATER_THAN:
LEA DX, GREATER_THAN_MSG
MOV AH, 9
INT 21h

JMP Exit

MAIN ENDP
END MAIN

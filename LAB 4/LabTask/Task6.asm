.MODEL SMALL
.STACK 100h
.DATA

PASSWORD DB 'abc$'
PASSWORD_CHECK DB 'abc$'
MATCHED_MSG DB 'Password matched$'
NOT_MATCHED_MSG DB 'Password not matched$'

.CODE

MAIN PROC

MOV AX, @DATA
MOV DS, AX

LEA SI, PASSWORD
LEA DI, PASSWORD_CHECK
MOV CX, 4
COMPARE_LOOP:
    MOV AL, [SI]
    MOV BL, [DI]
    CMP AL, BL
    JNE NOT_MATCHED
    INC SI
    INC DI
    LOOP COMPARE_LOOP

LEA DX, MATCHED_MSG
JMP PRINT_MSG

NOT_MATCHED:
LEA DX, NOT_MATCHED_MSG

PRINT_MSG:
MOV AH, 9
INT 21h

MOV AH, 4Ch
INT 21h

MAIN ENDP
END MAIN

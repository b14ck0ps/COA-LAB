.MODEL SMALL
.STACK 100h
.DATA

MSG DB 0AH,0DH,'HELLO WORLD$'
BYE_MSG DB 0AH,0DH,'BYE WORLD$'

.CODE

MAIN PROC

MOV AX,@DATA
MOV DS,AX

MOV CX,5

PRINT_LOOP:
LEA DX, MSG
MOV AH, 9
INT 21h
DEC CX

CMP CX, 0 
JE PRINT_BYE 

JMP PRINT_LOOP 

PRINT_BYE:
LEA DX, BYE_MSG
MOV AH, 9
INT 21h

Exit:
MOV AH,4CH
INT 21h

MAIN ENDP
END MAIN
